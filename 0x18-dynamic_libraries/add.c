/**
* sum - perform addition
* @a: first operand
* @b: second operand
* Return: a+b
*/
int add(int a, int b)
{
	return (a + b);
}
/**
* mod - perform addition
* @a: first operand
* @b: second operand
* Return: a+b
*/
int mod(int a, int b)
{
	return (a % b);
}
/**
* div  - perform addition
* @a: first operand
* @b: second operand
* Return: a+b
*/
int div(int a, int b)
{
	int dev = a/b;
	return (dev);
}
/**
* sub  - perform addition
* @a: first operand
* @b: second operand
* Return: a+b
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
* mul  - perform addition
* @a: first operand
* @b: second operand
* Return: a+b
*/
int mul(int a, int b)
{
	return (a * b);
}

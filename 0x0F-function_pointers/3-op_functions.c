#include "3-calc.h"
/**
* op_add - addition function
* @a: operand
* @b:operand
* Return: sum
*/
int op_add(int a, int b)
{
	int i;

	i = a + b;
	return (i);
}
/**
* op_sub - addition function
* @a: operand
* @b:operand
* Return: difference
*/
int op_sub(int a, int b)
{
	int i;

	i = a - b;
	return (i);
}
/**
* op_mul - addition function
* @a: operand
* @b:operand
* Return: multiple
*/
int op_mul(int a, int b)
{
	int i;

	i = a * b;
	return (i);
}
/**
* op_div - addition function
* @a: operand
* @b:operand
* Return: devisor
*/
int op_div(int a, int b)
{
	int i;

	i = a / b;
	return (i);
}
/**
* op_mod - modulus
* @a: operand
* @b:operand
* Return: remainder
*/
int op_mod(int a, int b)
{
	i = a % b;
	return (i);
}
